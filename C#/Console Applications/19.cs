using System;
int r;
double ci, a;
System.Console.WriteLine("Enter the radius of the circle");
r = Convert.ToInt32(Console.ReadLine());
ci = 2 * 3.14 * r;
System.Console.WriteLine("Circumference of the circle is " + ci);
a = 3.14 * r * r;
System.Console.WriteLine("Area of the circle is " + a);
